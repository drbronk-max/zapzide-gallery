/** Build a rounded dark-square SVG favicon with the glyph centered, and apply it. */
export function setFavicon() {
  const { viewBox, path, color } = iconFor(FILTER_TERM);
  const [, , vw, vh] = viewBox.split(" ").map(Number);
  const scale = 60 / Math.max(vw, vh);
  const x = (100 - vw * scale) / 2;
  const y = (100 - vh * scale) / 2;
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 100 100"><rect width="100" height="100" rx="20" fill="#0b0b0c"/><g transform="translate(${x} ${y}) scale(${scale})"><path d="${path}" fill="${color}"/></g></svg>`;

  let link = document.querySelector<HTMLLinkElement>('link[rel="icon"]');
  if (!link) {
    link = document.createElement("link");
    link.rel = "icon";
    document.head.appendChild(link);
  }
  link.type = "image/svg+xml";
  link.href = `data:image/svg+xml,${encodeURIComponent(svg)}`;
}
